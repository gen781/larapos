    public function cari($nama)
    {
        $pelanggan = Produk::where('nama', 'ILIKE', "%{$nama}%")->get();
        $pelanggan->view_pelanggans = [
            'href' => '/api/pelanggan',
            'method' => 'GET'
        ];

        $response = [
            'status' => 'Sukses',
            'pesan' => 'Informasi Pelanggan',
